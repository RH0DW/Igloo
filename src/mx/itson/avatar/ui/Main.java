    private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarActionPerformed
        
        //Obtener datos
        String primerNombre = txtPrimerNombre.getText().trim().toUpperCase().replace(" ", "");
        String segundoNombre = txtSegundoNombre.getText().trim().toUpperCase().replace(" ", "");
        String primerApellido = txtApellidoPaterno.getText().trim().toUpperCase().replace(" ", "");
        String segundoApellido = txtApellidoMaterno.getText().trim().toUpperCase().replace(" ", "");
        String year = txtA単o.getText();
        String dia = txtDia.getText();
        String mes = txtMes.getText();
        String sexo = combSexo.getSelectedItem().toString();
        String entidad = combEntidad.getSelectedItem().toString();
        
        //Generacion de curp
        try{
            Generacion generacion = new Generacion();
            String dosLetrasApellidoPaterno = generacion.getFirstTwoCharacters(primerApellido);
            String unaLetraApellidoMaterno = generacion.getFirstCharacter(segundoApellido);
            
            String primeraLetraNombre; 
            if (segundoNombre.length() > 0){
                primeraLetraNombre = generacion.getFirstCharacter(segundoNombre);
            }else {
                primeraLetraNombre = generacion.getFirstCharacter(primerNombre);
            }
            
            String fecha = generacion.juntarFecha(dia, mes, year);
            String genero = generacion.getFirstCharacter(sexo);
            String consonanteApellidoPaterno = generacion.primeraConsonanteValida(primerApellido);
            String consonanteApellidoMeterno = generacion.primeraConsonanteValida(segundoApellido);
            String consonanteNombre = generacion.primeraConsonanteValida(primerNombre);
            String digitoA単o = generacion.verificarYear(year);
            
            //Imprimir resultados
            if (fecha.length() == 0||digitoA単o.length() == 0){
                lblResultado.setText("Error en los datos ingresados");
            }else {
                lblResultado.setText(dosLetrasApellidoPaterno + unaLetraApellidoMaterno + primeraLetraNombre + fecha + genero +
                        entidad + consonanteApellidoPaterno + consonanteApellidoMeterno + consonanteNombre + digitoA単o + "1");
            }
            
            
            
        }catch (Exception error){lblResultado.setText("Error en los datos ingresados"); }
                
        
        
        
       
    }//GEN-LAST:event_btnGenerarActionPerformed

